public class Conversion3 {
    public static void main(String[] args) {
        // Declaramos variable entera a 
        int a;
        // Declaramos variable doble b
        double b;
        // Inicializo a con el valor 7
        b = 7;
        // Inicializo la variable b con el valor de la variable a 
        a = (int)b;
        // Imprimo el valor de a
        System.out.println("Valor de a: " + a);
        // Imprimo el valor de b 
        System.out.println("Valor de b: " + b);
    }
}

