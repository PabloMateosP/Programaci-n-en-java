public class bucles8_for {
    public static void main(String[] args) {    
    // El bucle for se usa así:
    // int i;
    // for (i = 0; condición; i++[Es lo que nos saque del bucle incrementando o decrementando]){
    // ------------
    // }
    // Esto es un contador a más, si quisiesemos hacerlo a menos se haría igual que los otros ejemplos con el i--, i > -1.
    int i;
    for (i = 0; i < 5; i++ ) {
        System.out.println("i: " + (i + 1));
    } 
    }
}
